				<div class="section__text">
					JR大分駅から南東へ徒歩10分ほどの場所に、地元民でにぎわうウッドデッキの空間が誕生した。大分市が整備した「線路敷ボードウォーク広場」だ。鉄道の高架化で生じた線路跡地を利用しており、長さ440m、幅員15～27mと細長い形状が広場の特徴だ。最東端の展望デッキからは、隣地で復元が進む戦国大名・大友宗麟氏の館跡が見える。ウッドデッキは駅周辺と遺跡をつなぐ「歴史回廊」の役目を担う。<br>　改修前は、鉄道の高架橋が影を落とす殺風景な敷地だった。「デザインの手掛かりとなる土地の履歴や地形は失われていた。新しく物語を作る必要があった」。こう話すのは、市の公共空間デザインアドバイザーを務め今回のプロジェクトで企画から施工監修まで携わった九州大学大学院の樋口明彦准教授だ。物語のテーマに掲げたのが、大分の人々の暮らしを支えてきた鉄道があった時代の記憶を継承すること。そこで樋口准教授は、大分市産のデッキ材に古レールを組み合わせた「線路デッキ」を提案した。<br>　隣接する高架橋の線形に合わせて、緩やかなカーブを描くようにデッキを配置する。高架橋のない北側には、盛り土と樹木による「里山」を連続させた。住宅街への目線を遮る狙いがある。<br>　「レールの上を歩いていくうちに、いつの間にか遺跡にたどり着く『細長いタイムマシンのような空間』を造ろうと考えた」と、樋口准教授は振り返る。<br>　構想を実現する上で関係者を悩ませたのが、線路デッキの敷設だ。「市と設計者、施工者の皆で知恵を出し合い、実現可能な断面形状を詰めていった。現場ではミリ単位の施工精度が求められた」。こう明かすのは、大分市都市計画部まちなみ整備課の松尾裕治参事補だ。図面通りの曲率半径を出すために、専用の機械を使ってレールを現場で曲げながら、特注の金具で基礎コンクリートに固定。デッキ材はあらかじめ工場で小分けにして防腐加工しており、現場でサイズを調整できないので、レールの固定位置がずれるとはまらなくなる。そこで、施工時は1～2mピッチでレールの取り付け位置を管理した。<br>　素材にもこだわった。プラスチックの擬木などは使っていない。木の柱を使った照明や自然石の階段、レンガを使った擁壁などが、ちりばめられている。複雑な構造に下支えされたシンプルなデザインや資材の素朴さが、ノスタルジックな雰囲気を際立たせている。
