					　平成13年11月に立ち上げられた「厳原町中心地区美しいまちづくり研究会」にアドバイザーとして樋口が参加したのが本プロジェクトの始まりである。研究会では、タウンサーベイや街路整備をきっかけにして地域振興に成功した先進地調査を行い、住民のまちづくりに対する意識を向上させて、「厳原中心地区の美しいまちづくりに関する提案書」を作成した。この提案書の内容は、後に長崎県が「長崎県美しいまちづくり推進条例」を策定するための参考にされた。<br>　平成14年7月、研究会を母体とした住民主体の「厳原町美しいまちづくり推進委員会」が立ち上げられ、その下部組織として「街路部会」が設置された。街路部会では、厳原町のメインストリートである大町通りとそれに直交する横町線の整備内容を検討した。本研究室の役割は、住民からのアイデアをもとに、図面や模型を使って専門的な視点で検討したものを部会に提案するというルーティンを整備内容が決まるまで繰り返し行った。約3年間、月に1回ペースの部会を継続的に開催し、平成19年3月に大町通りと横町線が竣工した。<br>　本プロジェクトのテーマは、住民をどこまで主体的に参加させることができるか。1つは、実寸大模型が功を奏した。舗装材や横断防止柵、フットライト、橋の高欄に至るまで、街路を構成するほぼ全てのエレメントを実寸大で住民に確認してもらった。自分たちが何ヶ月もかけて喧々諤々議論してきたものが実寸大で形になるという体験を繰り返すうちに、住民たちの議論の熱や質が上がっていった。また、粘り強く継続的に街路部会を開催したことも住民を主体的にさせる要因であった。発注者である長崎県も厳原町も住民参加の経験はほとんどない中で、始めのうちは手探りだったが、30回以上の部会を経て、参加者たちは互いを理解し率直な議論のできるパートナーヘと成長していった。その結果、多数の住民の協力を得ることができ、側道から電線を引くなどの工夫をして、電柱の数を激減させることができた。<br>　専門家が構造物をデザインするだけでは、まちはそうそう元気にならない。デザインという作業を通して住民たちをいかに「その気」にさせるかが、こういった取り組みでは重要である。街路と人材という種が蒔かれた厳原の今後が楽しみである。（伊東和彦　H18年修士課程修了）
