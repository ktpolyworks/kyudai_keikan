				<div class="section__text">
					白井地区は津和野町の中心地からやや離れた山間に位置し、川沿いを疾走する“SLやまぐち号”の応援を通じて地域活性化に取り組んでいた。しかし、平成25年7月の豪雨災害によって川と線路は寸断、災害復旧事業に着手することになった。災害復旧事業は、被災後の混乱の中、時間的制約の中で行わなければならない。さらに、山間地を流下する急勾配河川は洪水時の流れが激しく、魅力ある川を再生するためには幾つもの技術的課題を克服する必要がある。本作品では、このような課題を乗り越え、治水と環境が調和した川づくりを実現した。令和元年8月末の日曜日の朝現地を訪れた。川幅は全体的に広く設定され、川の地形回復に十分な空間が確保されている。形成された礫河原や水際に繁茂した植物はその証左だろう。精緻に積まれた野面石護岸には色相のばらつきもあり見事。石材が途中で変化しているのは制約のある中で石を積んだ苦労の跡である。護岸の目地空隙には奥行感があり、生き物の気配を感じ取ることができる。曲線的な護岸線形、水辺にアプローチする階段、樋管吐口の処理、水路に掛かる桁橋等細かい部分への配慮も修逸である。驚くべきは、下流側に配置された三段の石で組んだ落差工である。斜路部分は流水が横方向に分散しないよう澪筋部を設け、その下流は水深を確保して淵の機能を持たせている。魚の遡上も容易だろう。水理検討そして樹木医のアドバイスを受けて残したケヤキの大木には生命を大切にした設計者の意図を感じる。昼になり“SLやまぐち号”が白煙を上げながら疾走して行く。発災から6年、背景の山々や名賀川と調和した綻びのない風景に深く感動する。復旧・復興においても、求められるのは“地域を豊かにするデザイン”。この作品からは、そんなメッセージが聞こえて来る。<br>
					（国研）土木研究所 水環境研究グループ長　萱場 祐一）<br>
					　山々に囲まれた名賀川をやや高い位置から見下ろすと、地域の暮らしに馴染んだ素朴な川という印象を抱く。ところがここは、2013年の豪雨によって甚大な被害を被り、迅速に復旧が進められた流域の一部だという。上下流の区間は標準的なルールに素直に従って整備したのであろう画一的な河川空間になっており、良くも悪くも強いコントラストが生じている。<br>
					　川沿いをゆっくり歩くと、さまざまな要素に微細な変化が織り込まれていることに気付く。幅や高さの揺らぎが生み出す水流の表情、大小の曲線が複雑に混在するライン、当該地域に見られる手法を引用した石積みなどからは、この場所における川のありようをしっかり見据えて整備が進められたことが読み取れる。特に、手の込んだ頭首工の造形と災害を乗り越えて残されたケヤキの存在は、印象的な風景を創り出している。また、水路をまたぐ小さな橋梁や川面に近づく階段工のおさまりなどへの気配りからは、構造物の存在感をできるだけ消して、長い時間をかけて地域の風景に馴染ませようとする意図が垣間見える。<br>
					　この対象は、災害への緊急対策と地域景観の創出が、トレードオフの関係ではなく高度に両立可能であることを示している。つまり、災害が頻発する国土において今後も生じるであろう緊急的な整備のメルクマールとしての役割が期待できる。今回はその点が大きな評価ポイントとなった。今後は石積みの風合いが増していくとともに、SLやまぐち号へのフォトスポットとして来訪者を迎え入れる防災広場の設えなどにも好影響が生まれることを期待したい。<br>
					（千葉工業大学創造工学部デザイン科学科　教授　八馬 智）<br><br>
