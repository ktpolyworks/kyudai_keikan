				<div class="section__text">
					今、ダムがブームである。2007年から始まったダムカードで火が付き、現在は国も工事段階から観光に活用するダムツーリズムに積極的だ。土木事業が国民に好意的に認知されるために、観光や景観・デザインは重要な役割を果たしている。<br>
					　嘉瀬川ダムの第一印象は、“コンクリート重力式越流型ダムの理想形が誕生した”であった。これすなわち、設計者が言う「機能に忠実な姿の追求」、「機能と無関係な形態操作や意匠を排除」の完璧な成果である。国道323号の沿道植栽の隙間が、本ダムの良好な視点場である。そこから“天端道路を支える橋脚が全長にわたり均等に配置された”全面越流式の堤体が一望できる。一般にバラバラと出現する付属施設群は、その必要機能を咀嚼して最小化・形態調整され、橋脚のリズム感等を阻害しない位置に配置されている。実はこれらは、決して機能追求のみでは実現しない。堤体や施設の構造と、造形センスを併せ持つ技術者のみに実現できる特別なデザインなのである。<br>
					　それにしても堤体以外の湖畔空間や橋梁・トンネル等の道路施設は、景観に無配慮なのが残念である。この受賞を機に、ダムカードの堤体下の視点場やダムの駅など、ダム周辺を観光目線で再整備しては如何であろうか。<br>
					（大日本コンサルタント株式会社　髙楊 裕幸）<br>
					　全面越流式の堤体がそびえ立つ。ダム天端の管理橋を支えるピアが均等に配置されていて、下流側から見た姿は美しい。下流側面から見るとシャープなピアが連続し、その重なりもまた美しい。ピアの均等配置による越流部の分節が、コンクリートの巨大な塊であるダム本体にいいリズムを与えている。<br>
					　当初設計では、選択取水塔や予備ゲート操作室、エレベーター塔屋などの主要付属施設が、個別に設計・配置されているためにダム全体の景観的なまとまりがなかったという。当初案と最終案を比較した図面を見ると、その違いは明確である。設備や操作室スペースをコンパクトにするなどして、バランスのとれた大きさの構造物に変更し、均等配置したピアの間隔にすっきり収まるようにデザインされている。設備関係やメンテナンス関係担当者との粘り強い議論を経て実現できたダム本体のデザインである。<br>
					　管理庁舎や予備ゲート室の駆体は、ダム本体と同じ質感の現場打ちコンクリートとしている。建築的ではなく、どっしりとした感じである。評価は分かれたが個人的にはきらいではない。設計範囲に含まれる入口の芝生広場は殺風景で魅力に乏しいように見えた。もう少し工夫できたのではないか。残念に思った。<br>
					（株式会社 吉村伸一流域計画室　吉村 伸一）
