				明治以降、直方市は近隣の筑豊炭鉱から採掘される石炭の集積地として発展を遂げたが、川の水で石炭とボタを選別する“洗炭”により遠賀川は「ぜんざい川」と呼ばれる黒く濁った川となった。今となっては川の色も戻り、「チューリップフェア」や 「のおがた夏祭り」等のイベントで河川敷は賑わいを見せている。<br>　ここでの川づくりは市民団体・直方川づくり交流会によって遠賀川夢プラン2000が提案されたことに始まり、本事業の実施に際し、「改修デザインの検討で終わらせるのではなく、遠賀川を活用してまちづくりに貢献できるような仕組みづくりを市民と一緒になって考えていきたい」という、当時河川事務所の副所長であった田上氏の発意により、国土交通省遠賀川河川事務所・周辺住民・アドバイザーで構成される市民部会が設置されることとなった。<br>　まず最初の2年間の大きな検討テーマは、「まちと川の関係について考える」こと。そして、「改修デザインについて考える」こと。部会メンバーでの議論や粘土模型いじりでの具体的な整備イメージの提案を受けて、河川改修の基本構想ができた。当研究室は、部会での検討のネタとなる資料や模型を準備するとともに、市民から得られた意見をもとに、河川事務所及びコンサルタントと協同してデザイン案を作成する役割を担った。<br>　河川改修事業が予算化されると、河川事務所・コンサルタント・当研究室の間で該当区間の詳細デザインを検討し、実施案を練った。実施案は、市民部会での了承を得た後に最終決定され、逐次工事を実施している。現在までで、平成17年度と平成19年度の2回の工事を終えて、右岸側・約300m、左岸側・約1000m区間の整備が完了。こうして少しずつ、そして着実に整備が進んでいる。<br>　設計のポイントは、「川を身近に感じられ、広い範囲で自由に利用できる河川敷」への改修を求める市民部会からの声を反映し、左岸側では今までの複断面構造の低水部コンクリートブロック護岸を撤去して、高水敷を緩傾斜スロープ化し、水面までなだらかにつながる断面を採用したこと。右岸側は、河川構造上流水による掘削の恐れがあったため、こちらは見通しが確保でき、かつ安全なひな壇型の低水護岸を採用し、水際は階段護岸やカヌー乗場など親水性の高い空間とした。（吉岡聖貴　H21修士課程修了）

