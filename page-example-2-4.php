					もし身近な土木構造物が、みな勘六橋とおなじように、誠実でていねいに設計され、つくられていれば、それだけで身のまわりの景観はずいぶんよくなるだろう。そんな好感を抱かずにはいられない仕事である。<br>
					　下流側の沈下橋から眺める、水辺の空間と過不足なく調和している勘六橋の姿形が、設計者が目指した価値を余すところなく語っていて、とても心地よい。合理的で無駄のないプロポーションと、全体をスマートに見せるさりげない造形上の配慮の数々は、そのまま橋梁デザインのお手本になると思う。<br>
					　また、とくに共感したのは、下流側のみ歩道の中間地点に設けられた小さなバルコニーで、おおらかで魅力的な「直方の水辺」（2009年度最優秀賞受賞）を眺めるちょっとした展望場になっている。さりげないデザインの主張だが、橋の上から直方の水辺へと意識をつなぐ、この場所ならではの効果的な装置となっている。<br>
					　一方で、親柱や高欄、照明などのファニチュアのデザインの精度、路面や橋詰部の空間造形に、少々残念な印象が残った。構造物本体がとてもシンプルで合理的な思想でできているだけに、人の身体が触れる部分のディテールの荒さが、むしろ目立ってしまうのかもしれない。総合的な完成度を評価して、優秀賞の授賞となった。<br>
					（東京大学大学院工学系研究科社会基盤学専攻 教授　中井 祐）<br>
