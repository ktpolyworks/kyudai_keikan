						　同検討会では、福岡県により既に設計が進められていた橋長214m、幅員17m、3径間連続箱桁・両端部単純中空床版橋を出発点とし、同一の設計条件、事業費、工期等のもとで議論を開始。主役である河川景観の中にあって、遠景として目立たず軽い印象の橋、近景として威圧感のない橋を目標に据え、橋梁本体（主桁、橋脚、橋台）を主対象に最適解を模索しました。スタディでは敢えてCGを使わず、模型を用いて高水敷上からの眺めを吟味し、位置を変えてまた吟味するという手法を繰り返しています。<br>
						　主桁を扁平アーチの連続桁としリズム感のある軽快な姿にすること、モーメントバランスと審美的なバランスから最適なスパン割りとすること、二主箱桁構造を採用し桁側壁にテーパーを持たせることで主桁の幅を絞り、桁下空間の圧迫感を低減すること、合わせて橋脚のボリュームを最小化し対岸への視線の抜けを確保すること等、現場打ちのコンクリート構造物として可能な様々な工夫を行いました。<br>
						　本橋の構造形式やデザインは決して特殊なものではありませんが、どうすれば主役の川の風景の中に普通の桁橋をさりげなく置くことができるかという問いに対して、定石の範囲で可能な最適解を実現することができました。<br>
						　竣工後、直方北九州自転車道が開通し、川辺を利用する市民の数はさらに増加傾向にあります。この「普通の橋」が、遠賀川の美しい風景の脇役としていつの間にか馴染んでくれることを願っています。<br><br>
