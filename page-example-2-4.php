					　本橋の計画・設計では、通常は経済的で施工性にも優れる工場製作でプレキャスト部材の鋼桁やＰＣ桁が採用される。実際、連続する東勘六橋や上下流に架かる新橋と日ノ出大橋はＰＣポステンＴ桁橋である。これを工費増は抑えつつ、河川敷を利用する人に配慮し、遠景にはリズミカルな変断面の薄いシルエットで、中景では大きな張出し床版による陰影で桁高を薄く見せ、壁式橋脚で張出し梁を設けずにスッキリと、近景では、排水管を見せない工夫、大きな張出し床板と橋脚のスリットで圧迫案を軽減し、広々とした遠賀川の景観に溶け込ませた。<br>
